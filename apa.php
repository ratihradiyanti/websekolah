  <p>&nbsp;</p>
  <p>Apa Itu Pemanasan Global    </p>
  </center>
</h3>
<p>&nbsp;</p>
<p>Tanggal  : 16/08/2009, dibaca 269 kali.
</p>
<hr />
<div>&quot;Panas   banget ya hari ini!” Seringkah Anda mendengar pernyataan tersebut   terlontar dari orang-orang di sekitar Anda ataupun dari diri Anda   sendiri? Anda tidak salah, data-data yang ada memang menunjukkan planet   bumi terus mengalami peningkatan suhu yang mengkhawatirkan dari tahun ke   tahun. Selain makin panasnya cuaca di sekitar kita, Anda tentu juga   menyadari makin banyaknya bencana alam dan fenomena-fenomena alam yang   cenderung semakin tidak terkendali belakangan ini. Mulai dari banjir,   puting beliung, semburan gas, hingga curah hujan yang tidak menentu dari   tahun ke tahun. Sadarilah bahwa semua ini adalah tanda-tanda alam yang   menunjukkan bahwa planet kita tercinta ini sedang mengalami proses   kerusakan yang menuju pada kehancuran! Hal ini terkait langsung dengan   isu global yang belakangan ini makin marak dibicarakan oleh masyarakat   dunia yaitu Global Warming (Pemanasan Global). Apakah pemanasan global   itu? Secara singkat pemanasan global adalah peningkatan suhu rata-rata   permukaan bumi. Pertanyaannya adalah: mengapa suhu permukaan bumi bisa   meningkat?</div>
<div> </div>
<div><strong>Penyebab Pemanasan Global</strong></div>
<div>Penelitian yang telah dilakukan para ahli selama beberapa dekade   terakhir ini menunjukkan bahwa ternyata makin panasnya planet bumi   terkait langsung dengan gas-gas rumah kaca yang dihasilkan oleh   aktifitas manusia. Khusus untuk mengawasi sebab dan dampak yang   dihasilkan oleh pemanasan global, Perserikatan Bangsa Bangsa (PBB)   membentuk sebuah kelompok peneliti yang disebut dengan International   Panel on Climate Change (IPCC). Setiap beberapa tahun sekali, ribuan   ahli dan peneliti-peneliti terbaik dunia yang tergabung dalam IPCC   mengadakan pertemuan untuk mendiskusikan penemuan-penemuan terbaru yang   berhubungan dengan pemanasan global, dan membuat kesimpulan dari laporan   dan penemuan- penemuan baru yang berhasil dikumpulkan, kemudian membuat   persetujuan untuk solusi dari masalah tersebut . Salah satu hal pertama   yang mereka temukan adalah bahwa beberapa jenis gas rumah kaca   bertanggung jawab langsung terhadap pemanasan yang kita alami, dan   manusialah kontributor terbesar dari terciptanya gas-gas rumah kaca   tersebut. Kebanyakan dari gas rumah kaca ini dihasilkan oleh peternakan,   pembakaran bahan bakar fosil pada kendaraan bermotor, pabrik-pabrik   modern, peternakan, serta pembangkit tenaga listrik.</div>
<div> </div>
<div><strong>Apa itu Gas Rumah Kaca?</strong></div>
<div>Atmosfer bumi terdiri dari bermacam-macam gas dengan fungsi yang   berbeda-beda. Kelompok gas yang menjaga suhu permukaan bumi agar tetap   hangat dikenal dengan istilah “gas rumah kaca”. Disebut gas rumah kaca   karena sistem kerja gas-gas tersebut di atmosfer bumi mirip dengan cara   kerja rumah kaca yang berfungsi menahan panas matahari di dalamnya agar   suhu di dalam rumah kaca tetap hangat, dengan begitu tanaman di dalamnya   pun akan dapat tumbuh dengan baik karena memiliki panas matahari yang   cukup. Planet kita pada dasarnya membutuhkan gas-gas tesebut untuk   menjaga kehidupan di dalamnya. Tanpa keberadaan gas rumah kaca, bumi   akan menjadi terlalu dingin untuk ditinggali karena tidak adanya lapisan   yang mengisolasi panas matahari. Sebagai perbandingan, planet mars yang   memiliki lapisan atmosfer tipis dan tidak memiliki efek rumah kaca   memiliki temperatur rata-rata -32o Celcius.</div>
<div>Kontributor terbesar pemanasan global saat ini adalah Karbon   Dioksida (CO2), metana (CH4) yang dihasilkan agrikultur dan peternakan   (terutama dari sistem pencernaan hewan-hewan ternak), Nitrogen Oksida   (NO) dari pupuk, dan gas-gas yang digunakan untuk kulkas dan pendingin   ruangan (CFC). Rusaknya hutan-hutan yang seharusnya berfungsi sebagai   penyimpan CO2 juga makin memperparah keadaan ini karena pohon-pohon yang   mati akan melepaskan CO2 yang tersimpan di dalam jaringannya ke   atmosfer. Setiap gas rumah kaca memiliki efek pemanasan global yang   berbedabeda. Beberapa gas menghasilkan efek pemanasan lebih parah dari   CO2. Sebagai contoh sebuah molekul metana menghasilkan efek pemanasan 23   kali dari molekul CO2. Molekul NO bahkan menghasilkan efek pemanasan   sampai 300 kali dari molekul CO2. Gas-gas lain seperti   chlorofluorocarbons (CFC) ada yang menghasilkan efek pemanasan hingga   ribuan kali dari CO2. Tetapi untungnya pemakaian CFC telah dilarang di   banyak negara karena CFC telah lama dituding sebagai penyebab rusaknya   lapisan ozon.</div>
