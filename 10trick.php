    <p>10 Trik Belajar Efektif    </p>
  </center>
</h3>
<blockquote>
  <p>Tanggal  : 15-12-2011 04:48, dibaca 13513 kali.</p>
  <hr />
    <p>Mungkin   ada yang memercayai bahwa kesuksesan dalam belajar datang dengan   sendirinya. Tak salah memang, ketika ada yang meraih kesuksesan dalam   studinya, padahal ia terlihat tidak melakukan usaha yang sangat keras.   Akan tetapi, kebanyakan pelajar justru meraih kesuksesan dengan   mengembangkan dan mengaplikasikan kebiasaan belajar yang efektif? Nah,   10 trik berikut ini mungkin bisa jadi panduan bagi Anda yang tengah   mencari cara terbaik dalam belajar.<br />
      <br />
      <strong>1. Jangan mencoba untuk memaksakan belajar dalam satu sesi</strong><br />
      <br />
      Biasanya,   para pelajar yang sukses selalu meluangkan waktu belajarnya lebih   pendek dan jarang memaksakan mempelajari seluruhnya dalam satu atau dua   sesi. Kuncinya, belajarlah dengan konsisten dan lakukan secara reguler   meskipun dalam waktu singkat.<br />
      <br />
      <strong>2. Rencanakan saat Anda akan belajar</strong><br />
      <br />
      Jika   ingin sukses dalam belajar, susunlah jadwal dengan waktu yang spesifik   selama sepekan. Dan cobalah untuk tegas dengan jadwal yang telah Anda   buat. Mereka yang belajar secara sporadis, biasanya tidak berperforma   sebaik pelajar yang telah mengatur waktu belajarnya dengan disiplin.<br />
      <strong><br />
        3. Belajarlah pada waktu yang sama</strong><br />
      <br />
      Tidak   hanya apakah penting untuk merencanakan jadwal kapan harus belajar,   tetapi, Anda juga belajar untuk konsisten dengan rutinitas belajar   harian. Ketika Anda belajar pada waktu yang sama setiap hari dan setiap   minggu, maka hal itu akan menjadi bagian yang rutin dalam kehidupan   Anda. Secara mental dan emosional, Anda akan lebih mempersiapkan diri   saat sesi belajar tiba dan tentunya lebih produktif. <br />
      <strong><br />
        4. Setiap kegiatan belajar harus memiliki tujuan yang spesifik</strong><br />
      <br />
      Menganggap   sederhana belajar tanpa arahan yang jelas tidak akan efektif. Anda   perlu tahu dengan jelas apa yang Anda butuhkan dalam setiap kesempatan   belajar. Sebelum mulai belajar, aturlah tujuan dari belajar yang Anda   lakukan. Hal ini akan mendukung tujuan akademik secara keseluruhan.<br />
      <br />
      <strong>5. Jangan pernah menunda belajar</strong><br />
      <br />
      Adalah   hal yang sangat mudah umum untuk membatalkan sesi belajar yang telah   Anda rencanakan karena tidak tertarik dengan bidang studi, atau Anda   memiliki hal lain yang harus dilakukan, atau karena tugas yang diberikan   sangat sulit untuk dikerjakan. <br />
      <br />
      Pelajar yang berhasil tidak   pernah menunda waktunya untuk belajar. Jika Anda melakukannya, kegiatan   belajar Anda menjadi tidak efektif dan Anda tidak akan mendapatkan apa   yang dibutuhkan. Penundaan juga akan menimbulkan kekacauan dan menjadi   penyebab nomor satu dari kegagalan.<br />
      <br />
      <strong>6. Mulailah dengan pelajaran yang paling sulit</strong><br />
      <br />
      Tugas   atau pelajaran yang paling sulit akan membutuhkan usaha, mental, dan   energi yang paling besar. Anda sebaiknya memulai dengan hal ini. Sekali   Anda bisa menyelesaikan tugas yang paling berat ini, akan lebih mudah   untuk menyelesaikan sisanya. Percaya atau tidak, memulai dengan   pekerjaan yang paling sulit akan membawa peningkatan yang sangat besar   bagi keefektifan sesi belajar dan performa akademis Anda.<br />
      <br />
      <strong>7. Selalu review catatan Anda sebelum mulai mengerjakan tugas</strong><br />
      <br />
      Hal   yang pasti, sebelum Anda dapat mereview catatan yang dimiliki, maka   Anda harus memiliki catatan tersebut. Pastikan bahwa Anda selalu membuat   catatan yang baik selama di kelas. Sebelum memulai setiap sesi belajar   dan mengerjakan tugas utama yang harus diselesaikan, pastikan Anda tahu   bagaimana mengerjakannya dengan benar. <br />
      <br />
      <strong>8. Pastikan tidak ada gangguan selama belajar</strong><br />
      <br />
      Carilah   tempat belajar yang aman dari gangguan. Ketika Anda terganggu saat   belajar maka itu akan membuyarkan konsentrasi dan kegiatan belajar   menjadi tidak efektif. <br />
      <strong><br />
        9. Manfaatkan kelompok belajar dengan efektif</strong><br />
      <br />
      Pernah   mendengar pepatah, &quot;Dua kepala lebih baik daripada satu kepala?&quot;.   Pepatah ini bisa jadi benar untuk diterapkan dalam kegiatan belajar.   Belajar secara kelompok akan membawa sejumlah keuntungan, diantaranya,   mendapatkan bantuan dari pelajar lainnya saat Anda berjuang untuk   memahami sebuah konsep, menyelesaikan tugas dengan lebih cepat, dan   berbagi pengetahuan dengan pelajar lain yang akan membantu mereka dan   diri Anda sendiri untuk menginternalisasi persoalan. Tetapi, kelompok   belajar akan menjadi tidak efektif ketika tidak terstruktur dan anggota   grup minim persiapan. <br />
      <strong><br />
        10. Review catatan, tugas, dan materi lainnya setiap akhir pekan<br />
        </strong><br />
      Pelajar   yang sukses biasanya selalu mereview apa yang telah mereka pelajari   selama seminggu di setiap akhir pekan. Cara ini akan membuat mereka   mempersiapkan diri lebih baik untuk melanjutkan pembelajaran   konsep-konsep baru pada pekan berikutnya. <br />
      <br />
      Yakinlah, saat   menerapkan trik-trik ini dalam belajar akan membawa perubahan dan   peningkatan yang signifikan dalam catatan akademis dan keberhasilan   studi Anda. Kuncinya, jangan putus asa! </p>
  <p><em>(Sumber: Kompas.com)</em></p>
